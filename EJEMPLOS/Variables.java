package EJEMPLOS;

public class Variables {
    public static void main(String[] args) {
        // Declaración de variables
        int x = 144;
        int y = 999;

        // Mostrar los valores de las variables
        System.out.println("El valor de x es: " + x);
        System.out.println("El valor de y es: " + y);

        // Operaciones
        int suma = x + y;
        int resta = x - y;
        int division = x / y;
        int multiplicacion = x * y;

        // Mostrar los resultados
        System.out.println("La suma de x e y es: " + suma);
        System.out.println("La resta de x e y es: " + resta);
        System.out.println("La división de x entre y es: " + division);
        System.out.println("La multiplicación de x por y es: " + multiplicacion);
    }
}
